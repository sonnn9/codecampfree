public class WhileDoDemo {
    public static void main(String[] args) {
        int i =0;
        while(i < 3){
            System.out.println(i + " ");
            i++;
        }
        System.out.println();

        int j =0;
        do {
            System.out.println(j + " ");
            j++;
        } while (j < 3);
    }
}
